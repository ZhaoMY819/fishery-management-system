	<div class="add-update-preview">
		<el-form
			class="add-update-form"
			ref="ruleForm"
			:model="ruleForm"
			:rules="rules"
			label-width="180px"
			>
			<el-form-item class="add-item" label="用户账号" prop="yonghuzhanghao">
				<el-input v-model="ruleForm.yonghuzhanghao" 
					placeholder="用户账号" clearable :disabled=" false  ||ro.yonghuzhanghao"></el-input>
			</el-form-item>
			<el-form-item class="add-item" label="用户姓名" prop="yonghuxingming">
				<el-input v-model="ruleForm.yonghuxingming" 
					placeholder="用户姓名" clearable :disabled=" false  ||ro.yonghuxingming"></el-input>
			</el-form-item>
			<el-form-item class="add-item" label="充值余额" prop="zhanghaoyue">
				<el-input-number v-model="ruleForm.zhanghaoyue" placeholder="充值余额" :disabled=" false ||ro.zhanghaoyue"></el-input-number>
			</el-form-item>
			<el-form-item class="add-item" label="充值时间" prop="chongzhishijian">
				<el-date-picker
					:disabled=" false  ||ro.chongzhishijian"
					value-format="yyyy-MM-dd HH:mm:ss"
